            <?php
            // Get all students
            $students = mysqli_query($conn, "SELECT * FROM students ORDER BY last_name ASC");

            if (mysqli_num_rows($students) > 0) {
              while ($student = mysqli_fetch_assoc($students)) {
                $fullname = $student['first_name'] . ' ' . $student['middle_name'] . ' ' . $student['last_name'];

                echo "<h5 class='mt-4'><strong>{$fullname}</strong> ({$student['section']})</h5>";

                // Fetch grades for this student
                $grades = mysqli_query($conn, "
                  SELECT g.grade, s.subject_name, s.units 
                  FROM grades g
                  JOIN subjects s ON g.subject_id = s.subject_id
                  WHERE g.student_id = {$student['student_id']}
                ");

                if (mysqli_num_rows($grades) > 0) {
                  echo "<table class='table table-bordered'>
                          <thead>
                            <tr>
                              <th>Subject</th>
                              <th>Units</th>
                              <th>Grade</th>
                            </tr>
                          </thead>
                          <tbody>";
                  $total = 0;
                  $count = 0;
                  while ($g = mysqli_fetch_assoc($grades)) {
                    echo "<tr>
                            <td>{$g['subject_name']}</td>
                            <td>{$g['units']}</td>
                            <td>{$g['grade']}</td>
                          </tr>";
                    $total += $g['grade'];
                    $count++;
                  }
                  $average = $count > 0 ? round($total / $count, 2) : 0;
                  echo "</tbody></table>";
                  echo "<p><strong>Average Grade:</strong> {$average}</p>";
                } else {
                  echo "<p class='text-muted'>No grades recorded yet.</p>";
                }
              }
            } else {
              echo "<div class='alert alert-warning'>No students found.</div>";
            }
            ?>
